namespace NyxGui;

/// <summary>Text alignment within a widget bounds rectangle.</summary>
public enum NyxTextAlign
{
    /// <summary>Top-left (default horizontal alignment).</summary>
    TopLeft = 0,

    /// <summary>Horizontally centered on the top edge.</summary>
    TopCenter = 1,

    /// <summary>Top-right.</summary>
    TopRight = 2,

    /// <summary>Centered horizontally and vertically (single-line titles, buttons).</summary>
    Center = 3,
}
